namespace AjaxControlToolkit
{
    /// <summary>
    /// Display the tabs on top of the panel or below (default is top)
    /// </summary>
    public enum TabStripPlacement
    {
        /// <summary>
        /// Render the tabs on top of the panel
        /// </summary>
        Top,

        /// <summary>
        /// Render the tabs below the panel
        /// </summary>
        Bottom,

        /// <summary>
        /// Render the tabs on top right of the panel
        /// </summary>
        TopRight,

        /// <summary>
        /// Render the tabs on bottom right of the panel
        /// </summary>
        BottomRight
    }
}